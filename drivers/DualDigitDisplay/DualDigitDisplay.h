class DualDigitDisplay {
public:
  ShiftRegister shiftReg;

  DualDigitDisplay(PinName dataPin, PinName clkPin, PinName latchPin) : shiftReg(dataPin, clkPin, latchPin) {
    // nothing
  }

  void init(int initialDisplayValue = 0) {    
    if (initialDisplayValue != 0) {
      write(initialDisplayValue);
    } else {
      shiftReg.writeByte16(0b0000000000000000); // all leds off
      shiftReg.pulseLatch();
    }
  }

  void write(int number) {
    if (number > 99) {
      shiftReg.writeByte16(DIGITS[0]);
      shiftReg.pulseLatch();
      return;
    } else if (number < 0) {
      shiftReg.writeByte16(DIGITS[0]);
      shiftReg.pulseLatch();
      return;
    }
    shiftReg.writeByte16(DIGITS[number]);
    shiftReg.pulseLatch();
  }
  

};


